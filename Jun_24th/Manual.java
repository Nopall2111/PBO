    private void enterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterBtnActionPerformed
        try {
            int nominal = Integer.parseInt(nominalF.getText());

            if (nominal <= 0) {
                JOptionPane.showMessageDialog(this, 
                    "Nominal harus lebih dari Rp0!", "Error", 
                    JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (CekSaldo.saldo - nominal < 50000) {
                JOptionPane.showMessageDialog(this, 
                    "Saldo tidak cukup! Minimal tersisa Rp50.000", "Error", 
                    JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Konfirmasi penarikan
            int pilihan = JOptionPane.showConfirmDialog(
                this, 
                "Tarik Rp" + nominal + "?", "Konfirmasi", 
                JOptionPane.YES_NO_OPTION
            );

            if (pilihan == JOptionPane.YES_OPTION) {
                CekSaldo.saldo -= nominal;

                JOptionPane.showMessageDialog(
                    this, 
                    "Sukses! Anda menarik Rp" + nominal + 
                    "\nSaldo tersisa: Rp" + CekSaldo.saldo, 
                    "Info", 
                    JOptionPane.INFORMATION_MESSAGE
                );

                this.dispose();
                new Menu().setVisible(true);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, 
                "Input harus angka!", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_enterBtnActionPerformed

