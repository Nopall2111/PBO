    private void totalBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalBtnActionPerformed
        // Integer.parseInt() -> Mengubah String menjadi int
        int harga = Integer.parseInt(hargaF.getText());
        int jumlah = Integer.parseInt(jmlF.getText());
        double diskon = 0;
        int tb = harga * jumlah;

        // Integer.toString() -> Mengubah int menjadi String
        totalF.setText(Integer.toString(tb));

        if(tb > 100000) {
            diskon = 0.1 * tb;
        }

        diskonF.setText(Double.toString(diskon));

        double tbayar = tb - diskon;

        bayarF.setText(Double.toString(tbayar));
    }//GEN-LAST:event_totalBtnActionPerformed

