    private void kembalianBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kembalianBtnActionPerformed
        double bayar = Double.parseDouble(uangF.getText());
        double tbayar = Double.parseDouble(bayarF.getText());
        if(bayar < tbayar) {
            JOptionPane.showMessageDialog(this, "Uang anda kurang", "Pesan", 
                    JOptionPane.INFORMATION_MESSAGE);
        }
        double kembali = bayar - tbayar;
        kembalianF.setText(Double.toString(kembali));
    }//GEN-LAST:event_kembalianBtnActionPerformed

